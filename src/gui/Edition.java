    private void JBAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAjouterActionPerformed
        // TODO add your handling code here:
        Proclamateur p=new Proclamateur();
        p.setNom(JTFNomP.getText().trim());
        p.setEMail(JTFEmail.getText().trim());
        p.setPhone1(JTFTel1.getText().trim());
        p.setPhone2(JTFTel2.getText().trim());
        p.setPhone3(JTFTel3.getText().trim());
        p.setSexe(jComboBoxSexe.getSelectedItem().toString());
        if(p.getSexe().replaceAll(" ", "").isEmpty()){
            if(JOptionPane.showConfirmDialog(this,"Vous n'avez pas s\u00E9lectionn\u00E9 le sexe de ce proclamateur. Voulez-vous continuer?")!=0){
                     return;
            
               }
        }
               
        //Obtenir le congregation
        p.setIdcongregation(listCong.get(JCBNomC.getSelectedIndex()));
        
        try{
            int phone1=Integer.parseInt(p.getPhone1());
            int phone2=Integer.parseInt(p.getPhone2());
            int phone3=Integer.parseInt(p.getPhone3());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Le num\u00E9ro de telephone doit etre un entier","Erreur de saisie",JOptionPane.ERROR_MESSAGE);
            return;
        }
        ad.getTransac().begin();
        ad.getEm().persist(p);
        ad.getTransac().commit();
        
        listProc=ad.getListe("Proclamateur.findAll");
        
        //on prepare les donnees pour affichage dans les tables
        
        new utilites.table.GestionTable().chargeTable(JTableProclamateur, new entite.Proclamateur().dataTable(listProc),new String[] { "Nom", "E_mail", "N° Tel. 1", "N° Tel. 2", "N° Tel. 3", "Congrégation"},new Class[] {java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class});
        //On vide les champs de textes
        new Utilitaire().videJTextField(new JTextField[]{JTFNomP,JTFEmail,JTFTel1,JTFTel2,JTFTel3});
    }//GEN-LAST:event_JBAjouterActionPerformed

