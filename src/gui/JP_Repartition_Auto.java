    private void tableRepartionAutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRepartionAutoMouseClicked
        // TODO add your handling code here:
        int rowS=tableRepartionAuto.getSelectedRow();
        String jour=tableRepartionAuto.getValueAt(rowS, 2).toString();
        jour=jour.substring(0, jour.indexOf(","));
//        System.out.println("jour "+jour);
        String leProclamateurSelectionner=tableRepartionAuto.getValueAt(rowS, 0).toString();
        if(evt.getClickCount()==2){
            procRechange=new entite.Planning().getProclamateurForChange(tableRepartionAuto.getValueAt(rowS, 3).toString(), tableRepartionAuto.getValueAt(rowS, 4).toString(),jour , s.getIdsite());
            Vector vi=new entite.Planning().getProclamateurForChange(tableRepartionAuto.getValueAt(rowS, 3).toString(), tableRepartionAuto.getValueAt(rowS, 4).toString(),jour , s.getIdsite());
           
//            System.out.println("selection "+procRechange);
            JD_Proc_Dispo jdpd=new JD_Proc_Dispo(null, true,vi);
            jdpd.setVisible(true);
            //on recherche le proclamateur qui a ete selectionne
            if(IDCHOIX!=-10){
                
                Proclamateur p=ad.getEm().find(Proclamateur.class, Integer.parseInt(procRechange.elementAt(IDCHOIX).firstElement().toString()));
                //on obtient la ligne du planning qui a ete selectionne
                Planning pl=(Planning)listPlanning.get(rowS);
                //modification dans la BDD
                pl=ad.getEm().find(Planning.class,pl.getIdplanning());
                pl.setIdproclamateur(p);
                ad.getTransac().begin();
                ad.getEm().flush();
                ad.getTransac().commit();
                //modification sur la table
                System.out.println("proclamateur "+p.getNom());
                tableRepartionAuto.setValueAt(p.getNom(), rowS, 0);
                tableRepartionAuto.getModel().setValueAt(p.getNom(), rowS, 0);
//                tableRepartionAuto.getModel().
                //modification dans l'etat
            }
            
            
            
        }else{//mise en evidence des doublons
            RenduTable_Doublon_De_Valeur rd=new RenduTable_Doublon_De_Valeur(leProclamateurSelectionner);
            tableRepartionAuto.setDefaultRenderer(String.class, rd);
            tableRepartionAuto.repaint();
        }
    }//GEN-LAST:event_tableRepartionAutoMouseClicked

