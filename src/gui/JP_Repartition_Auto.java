    private void JBPdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBPdfActionPerformed
        // TODO add your handling code here:
                        new Planning().doPlanningPdf(maxGroupe, s.getLieu());
                        InputStream reportStream = this.getClass().getResourceAsStream("/propriete/Planning.jrxml"); 
                        HashMap map=new HashMap();
                         Lis_Utilites lu=new Lis_Utilites("propriete/chemin.properties");
                         String chemin=lu.getValeurProperties("chemin").toString();
                         map.put("SUBREPORT_DIR",chemin);
                        
                        // Convert template to JasperDesign  
                        try{
                             JasperDesign jd = JRXmlLoader.load(reportStream);  
                  
                             // Compile design to JasperReport  
                             JasperReport jr = JasperCompileManager.compileReport(jd); 
                          
                            // Create the JasperPrint object  
                            // Make sure to pass the JasperReport, report parameters, and data source  
                            JasperPrint jp = JasperFillManager.fillReport(jr,map, new utilites.MaConnexion().getInstance());  
                            // JasperViewer jv = new JasperViewer(jp);  
                            JRViewer jrv=new JRViewer(jp);
                            jScrollPane1.setViewportView(jrv);
                            jScrollPane1.repaint();
                            JBPdf.setEnabled(false);

                        }catch(Exception e){
                              JOptionPane.showMessageDialog(this, ""+e.getMessage());
                        }
    }//GEN-LAST:event_JBPdfActionPerformed

